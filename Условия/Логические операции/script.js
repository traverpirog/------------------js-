// На самом деле необязательно использовать конструкцию if-else
// Оператора вида (==, ===, !=, <, > и т.д) сами возвращают true или false
// let a = 1;
// let b = 2;
// 
// if (a == b) {
// 	console.log(true); // Выведет true
