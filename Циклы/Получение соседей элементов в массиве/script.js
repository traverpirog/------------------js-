for (let i = 0; i < arr.length; i++) {
    if (i + 1 == arr.length) {
        console.log('Конец цикла! Не с чем сложить!');
    } else {
        console.log(i + '-я итерация','текущее значение - ' + arr[i] + ', следующее значение - ' + arr[i + 1], 'сумма - ' + (arr[i] + arr[i + 1]));
    }
}

