// Задача 1
// Дан массив с числами. 
// Переберите его циклом и в каждой итерации цикла 
// выведите следующий элемент массива.
let arr = [1, 2, 3, 4, 5];
for (let i = 0; i < arr.length; i++) {
    if (i + 1 == arr.length) {
        console.log(i + '-я итерация','значение - ' + arr[i], 'Конец цикла, возвращаюсь в начало - ' + arr[0]);
    } else {
        console.log(i + '-я итерация','значение - ' + arr[i], 'следующий элемент - ' + arr[i + 1]);
    }
}


// Задача 2
// Модифицируйте предыдущую задачу так, 
// чтобы в каждой итерации цикла выводилась 
// сумма текущего и следующего элемента массива.
